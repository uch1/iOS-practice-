// Why do optionals exist?

/*
 Rules:
 1. Every variable must be define (implicit/explicit)
 2. The type can be inferred based on the value
 3. Every normal type should have value associated
 
*/

// Implicit and Explicit type
let name: String = "Uchenna" // Explicit
let anotherName = "Uchenna" // Implicit

let age: Int = 40 // Explicit
let anotherAge = 40 // Implicit
let gpa: Double = 3.88 // Explicit
let myGPA = 3.01 // Implicit

let uchennaAge: Int? = 123
let garfieldAge: Int? = 40

//uchennaAge + garfieldAge

